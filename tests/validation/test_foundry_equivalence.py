"""Foundry output equivalence tests.

These tests load reference tensors generated by ``scripts/generate_foundry_reference.py``
(run inside the brineylab/ligandmpnn Docker container) and verify that our
ProteinMPNN and LigandMPNN implementations produce identical outputs given the
same weights and inputs.

The comparison is staged:
1. **Weight loading** — Our model loads the Foundry-converted state_dict with strict=True.
2. **Graph featurization** — Same backbone coords → same E_idx, same E.
3. **Encoder** — Same graph features → same h_V, h_E.
4. **Decoder** — Same encoder output + causal masks → same log_probs.
"""

from __future__ import annotations

import pytest
import torch

from teddympnn.models.protein_mpnn import ProteinMPNN
from tests.validation.conftest import load_reference, requires_reference_data

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _load_model_with_foundry_weights(ref: dict) -> ProteinMPNN:
    """Create a ProteinMPNN and load the Foundry-converted state_dict."""
    model = ProteinMPNN()
    model.load_state_dict(ref["state_dict"], strict=True)
    model.eval()
    return model


# ---------------------------------------------------------------------------
# ProteinMPNN equivalence
# ---------------------------------------------------------------------------


PROTEINMPNN_CASES = ["single_chain", "two_chain", "batch"]


@requires_reference_data
class TestProteinMPNNWeightLoading:
    """Verify Foundry state_dict loads into our model with strict=True."""

    def test_strict_load(self) -> None:
        ref = load_reference("proteinmpnn_foundry_state_dict")
        model = ProteinMPNN()
        # This must not raise
        model.load_state_dict(ref["state_dict"], strict=True)

    def test_state_dict_keys_match(self) -> None:
        ref = load_reference("proteinmpnn_foundry_state_dict")
        model = ProteinMPNN()
        our_keys = set(model.state_dict().keys())
        ref_keys = set(ref["state_dict"].keys())
        assert our_keys == ref_keys, f"Key mismatch: {our_keys.symmetric_difference(ref_keys)}"


@requires_reference_data
class TestProteinMPNNGraphFeaturization:
    """Verify graph featurization produces identical E_idx and E."""

    @pytest.mark.parametrize("case", PROTEINMPNN_CASES)
    def test_neighbor_indices(self, case: str) -> None:
        ref = load_reference(f"proteinmpnn_{case}")
        model = _load_model_with_foundry_weights(ref)

        # Foundry stores X_backbone (B, L, 4, 3) which is what we feed as X
        X = ref["input_features"]["X_backbone"]
        residue_mask = ref["input_features"]["residue_mask"].float()
        R_idx = ref["input_features"]["R_idx"]
        chain_labels = ref["input_features"]["chain_labels"]

        with torch.no_grad():
            result = model.graph_featurization_module(
                X=X,
                residue_mask=residue_mask,
                R_idx=R_idx,
                chain_labels=chain_labels,
                structure_noise=0.0,
            )

        assert torch.equal(result["E_idx"], ref["graph_features"]["E_idx"])

    @pytest.mark.parametrize("case", PROTEINMPNN_CASES)
    def test_edge_features(self, case: str) -> None:
        ref = load_reference(f"proteinmpnn_{case}")
        model = _load_model_with_foundry_weights(ref)

        X = ref["input_features"]["X_backbone"]
        residue_mask = ref["input_features"]["residue_mask"].float()
        R_idx = ref["input_features"]["R_idx"]
        chain_labels = ref["input_features"]["chain_labels"]

        with torch.no_grad():
            result = model.graph_featurization_module(
                X=X,
                residue_mask=residue_mask,
                R_idx=R_idx,
                chain_labels=chain_labels,
                structure_noise=0.0,
            )

        torch.testing.assert_close(result["E"], ref["graph_features"]["E"], atol=1e-5, rtol=1e-5)


@requires_reference_data
class TestProteinMPNNEncoder:
    """Verify encoder produces identical h_V and h_E."""

    @pytest.mark.parametrize("case", PROTEINMPNN_CASES)
    def test_encoder_h_V(self, case: str) -> None:
        ref = load_reference(f"proteinmpnn_{case}")
        model = _load_model_with_foundry_weights(ref)

        # Feed the exact Foundry graph features to bypass featurization diffs
        input_features = {
            "residue_mask": ref["input_features"]["residue_mask"].float(),
        }

        with torch.no_grad():
            enc = model.encode(input_features, ref["graph_features"])

        torch.testing.assert_close(enc["h_V"], ref["encoder_features"]["h_V"], atol=1e-5, rtol=1e-5)

    @pytest.mark.parametrize("case", PROTEINMPNN_CASES)
    def test_encoder_h_E(self, case: str) -> None:
        ref = load_reference(f"proteinmpnn_{case}")
        model = _load_model_with_foundry_weights(ref)

        input_features = {
            "residue_mask": ref["input_features"]["residue_mask"].float(),
        }

        with torch.no_grad():
            enc = model.encode(input_features, ref["graph_features"])

        # Wider tolerance for h_E: Foundry uses gradient checkpointing in the
        # encoder which introduces ~2e-5 numerical diffs in edge features.
        torch.testing.assert_close(enc["h_E"], ref["encoder_features"]["h_E"], atol=5e-5, rtol=1e-4)


@requires_reference_data
class TestProteinMPNNDecoder:
    """Verify decoder produces identical log_probs given identical inputs."""

    @pytest.mark.parametrize("case", PROTEINMPNN_CASES)
    def test_log_probs(self, case: str) -> None:
        ref = load_reference(f"proteinmpnn_{case}")
        model = _load_model_with_foundry_weights(ref)

        input_features = {
            "S": ref["input_features"]["S"],
            "residue_mask": ref["input_features"]["residue_mask"].float(),
        }

        # Use the exact Foundry causal masks
        causality_masks = {
            "causal_mask": ref["decoder_features"]["causal_mask"],
            "anti_causal_mask": ref["decoder_features"]["anti_causal_mask"],
            "decoding_order": ref["decoder_features"]["decoding_order"],
        }

        with torch.no_grad():
            dec = model.decode_teacher_forcing(
                input_features=input_features,
                graph_features=ref["graph_features"],
                encoder_output=ref["encoder_features"],
                causality_masks=causality_masks,
            )

        torch.testing.assert_close(
            dec["log_probs"],
            ref["decoder_features"]["log_probs"],
            atol=1e-5,
            rtol=1e-5,
        )


